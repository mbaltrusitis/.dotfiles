      "format:: ## autoformat all files",
      ".PHONY: format",
      "",
      "precommit: ## run all pre-commit hooks manually",
      "\tpre-commit run --all-files",
      ".PHONY: precommit",
      "",
      "",
      "#> Utility.",
      "help: ## show this help",
      "\t@echo \"Specify a command from the list below:\"",
      "\t@echo \"\"",
      "\t@awk 'BEGIN {FS = \":.*?## \"}; \\",
      "\t\t/^#> / { \\",
      "\t\t\tgsub(/^#> /, \"\", $0); \\",
      "\t\t\tprintf \"\\n\\033[1;35m%s\\033[m\\n\", $0; \\",
      "\t\t\tnext \\",
      "\t\t} \\",
      "\t\t/^[0-9a-zA-Z_-]+:.*?## / { \\",
      "\t\t\tprintf \"  \\033[0;36m%-20s\\033[m %s\\n\", $1, $2 \\",
      "\t\t}' $(MAKEFILE_LIST)",
      "\t@echo \"\"",
      ".PHONY: help",
      "",
      "",
    }),
  }),
}
