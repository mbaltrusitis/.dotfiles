local ls = require("luasnip")
local s = ls.snippet
local t = ls.text_node
local i = ls.insert_node

return {
  s("boilerplate", {
    t({
      "# vim: filetype=make",
      ".ONESHELL:",
      "",
      "OS := $(shell uname -s | tr '[:upper:]' '[:lower:]')",
      "ENV_FILE := \"env_file\"",
      "COMPOSE := docker compose",
      "",
      "export OS := $(OS)",
      "",
      "#> Setup.",
      "",
      "setup:: ## setup the development environment",
      "setup:: precommit-install",
      ".PHONY: setup",
      "",
      "precommit-install: ## install pre-commit hooks",
      "\tpre-commit install",
      "",
      "#> Docker.",
      "",
      "docker-build-all::  ## build all docker stages",
      "docker-build-all:: docker-build-base",
      "docker-build-all:: docker-build-deps",
      ".PHONY: docker-build-all",
      "",
      "docker-build-%:",
      "\tdocker build -t clickhouse-dbt-$* --target $* .",
      ".PHONY: docker-build-%",
      "",
      "compose-build: ## build the development env within docker comopose",
      "\t$(COMPOSE) build",
      ".PHONY: compose-build",
      "",
      "up: ## bring up the stack",
      "\t$(COMPOSE) up -d",
      "\t$(COMPOSE) attach clickhouse",
      ".PHONY: up",
      "",
      "down: ## pause the stack",
      "\t$(COMPOSE) down",
      ".PHONY: down",
      "",
      "compose-teardown: ## stop the stack, deleting orphans and volumes",
      "\t$(COMPOSE) down -v --remove-orphans",
      ".PHONY: compose-teardown",
      "",
      "#> Lint and Format.",
      "",
      "lint:: ## lint all files",
      ".PHONY: lint",
